// @lc code=start
/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @return {number[]}
 */

var inorderTraversal = function (root) {
  if (!root) return []
  let head = []
  function order(node) {
      if (node) {
          order(node.left)
          head.push(node.val)
          order(node.right)
      }
  }
  order(root)
  return head
};
